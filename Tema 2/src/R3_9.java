
public class R3_9 {

    public static void main(String[] args) {
        System.out.println("Este programa te muestra la tabla de multiplicar del 1 al 10");

        System.out.println();

        for (int a = 1; a <= 10; a++) {

            System.out.println("Tabla del " + a);

            for (int i = 1; i <= 10; i++) {

                System.out.println(a + " * " + i + " = " + a * i);

            }

            System.out.println();

        }


    }
}
