package R2_2;

public class Tarjeta_gráfica {

    public String marca;
    public String modelo;
    public int númeroDeNúcleos;
    public float velocidad;
    public int cantidadDeMemoria;

    public Tarjeta_gráfica(String marca, String modelo, int númeroDeNúcleos, float velocidad, int cantidadDeMemoria) {
        this.marca = marca;
        this.modelo = modelo;
        this.númeroDeNúcleos = númeroDeNúcleos;
        this.velocidad = velocidad;
        this.cantidadDeMemoria = cantidadDeMemoria;

    }

}
